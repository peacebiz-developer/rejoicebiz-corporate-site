# ローカルプレビュー用起動スクリプト
# Node.js (npx) を使用して、最も安全で高速なローカルサーバーを立ち上げます。
#
# npx serve の特徴：
# - 余計なキャッシュを持たず、設定不要でファイル構成の通りにルーティングします。
# - ディレクトリ型URL（例：/company/）にアクセスした際に自動で index.html を返します。
# - MIMEタイプ（ダウンロードになってしまう問題）の判定を正確に行います。

Write-Host "ローカルサーバー（npx serve）を起動します..."
Write-Host "ブラウザで表示される Local: http://localhost:3000 にアクセスしてください。"
Write-Host "停止するには Ctrl+C を押してください。"
Write-Host "------------------------------------------------------"

npx serve .
